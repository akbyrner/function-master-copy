function addFriend(name, object) {
    if (!object.friends) {
      object.friends = []; // Initialize the friends array if it doesn't exist
    }
    object.friends.push(name);
    return object;
  }

//////////////////////////////////////////////////////////////////////
// Function 12 - Is Friend ///////////////////////////////////////////
//////////////////////////////////////////////////////////////////////

function isFriend(name, object) {
    if (!object.friends) {
      return false; // If there are no friends, they can't be a friend
    }
  
    return object.friends.includes(name);
  }

